let chosenList = [];

let nameGen = document.getElementById("name");
let nameGenBtn = document.getElementById("nameGenBtn")
let list1Btn = document.getElementById("list1");
let list2Btn = document.getElementById("list2");
let list3Btn = document.getElementById("list3");
let list4Btn = document.getElementById("list4");




list1Btn.addEventListener("click", BreakfastBtnChecked);
list2Btn.addEventListener("click", LunchBtnChecked);
list3Btn.addEventListener("click", LevesBtnChecked);
list4Btn.addEventListener("click", DessertBtnChecked);




nameGenBtn.addEventListener("click", NameGenerator);

function NameGenerator(){
    
    if(list1Btn.checked){
        let randomNumber = Math.floor(Math.random() * breakfastz.length);
       nameGen.innerText = breakfastz[randomNumber]; }
    
    if(list2Btn.checked){
        let randomNumber = Math.floor(Math.random() * lunch.length)
        nameGen.innerText = lunch[randomNumber]; }
    
        if(list3Btn.checked){
        let randomNumber = Math.floor(Math.random() * leves.length)
        nameGen.innerText = leves[randomNumber]; }
        
        if(list4Btn.checked){
        let randomNumber = Math.floor(Math.random() * desszert.length)
        nameGen.innerText = desszert[randomNumber]; }
        




}


function chooseList(list){
    chosenList = list
}


function BreakfastBtnChecked(){
    if (list1Btn.checked){
        list1Btn.checked = true;
        list2Btn.checked = false;
        list3Btn.checked = false;
        list4Btn.checked = false;
    }
    chooseList(Breakfastz);
}
function LunchBtnChecked(){
    if(list2Btn.checked){
        list2Btn.checked = true;
        list1Btn.checked = false;
        list3Btn.checked = false;
        list4Btn.checked = false;
    }
    chooseList(Lunch);
}

function LevesBtnChecked(){
    if(list3Btn.checked){
        list2Btn.checked = false;
        list1Btn.checked = false;
        list3Btn.checked = true;
        list4Btn.checked = false;
    }
    chooseList(Leves);
}

function DessertBtnChecked(){
    if(list4Btn.checked){
        list2Btn.checked = false;
        list1Btn.checked = false;
        list3Btn.checked = false;
        list4Btn.checked = true;
        
    }
    chooseList(Leves);
}


    

